// Javascript kinds of tow
// 1. primitive data type
// 2. non-primitive data type

/**
 * primitive data type kind of 5;
    1. String   => represent sequence of characters e.g. "hello world"/'hello world'
    2. Number   => represent numeric values e.g. 100/10.3
    3. Boolean  => represent boolean value either false or true
    4. Undefined=> represent undefined value
    5. Null     => represent null i.e no value at all
 */
var myName = 'FAKHRUL ISLAM'        //String data type
var myPhoneNumber = 01738922671;    //Number data type
var whoIsHe = true;                 //Boolean data type
var u;                              //Undefined data type
var n = null;                       //Null data type
console.log(myName)
console.log(myPhoneNumber)
console.log(whoIsHe)
console.log(u)
console.log(n)